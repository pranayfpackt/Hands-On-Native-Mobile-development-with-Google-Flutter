/** 
 * equality and relational operators
 */
main() {
    var number = 3;
    
    print(number == 3);  // prints true
    print(number != 3);  // prints false
    print(number > 3);   // prints false
    print(number < 3);   // prints false    
    print(number >= 3);  // prints true
    print(number <= 3);  // prints true

    var stringOne = "Some String";
    var stringTwo = "Some String";
    print(stringOne == stringTwo);
}