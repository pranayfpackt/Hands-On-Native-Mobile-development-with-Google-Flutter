/**
 * if else statement
 */
main() {
  var isItABug = false;

  if (isItABug) {
    print("It’s a bug.");
  } else {
    print("It’s not a bug. It’s an undocumented feature!");
  }

// Equivalent to using the already learned expression:
  print(isItABug
      ? "It’s a bug."
      : "It’s not a bug. It’s an undocumented feature!");


// on multiple       
}
